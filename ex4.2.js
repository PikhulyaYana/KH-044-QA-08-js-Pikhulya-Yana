/*Write a program which take number of month from user and 
print out name of season (summer, winter ...)*/


let month = prompt('Enter the number of month');
if (res = (month ==12) || (month >=1 && month <=2)) {
    console.log ("Winter");
}
if (res = month >=3 && month <=5) {
    console.log ("Spring");
}
if (res = month >=6 && month <=8) {
    console.log ("Summer");
}
if (res = month >=9 && month <=11) {
    console.log ("Fall");
}
